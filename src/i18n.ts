function applyLanguageState(language: string) {
  const normalized = normalizeAppLanguageCode(language);

  if (typeof window !== "undefined") {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, normalized);
    document.documentElement.lang = normalized;
  }
}

applyLanguageState(initialLanguage);
i18next.on("languageChanged", applyLanguageState);

export function normalizeAppLanguage(
  language: string | null | undefined,
): AppLanguage {
  return normalizeAppLanguageCode(language);
}

